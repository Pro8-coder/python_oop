"""
Объявите класс Line, объекты которого создаются командой:

line = Line(x1, y1, x2, y2)
где x1, y1, x2, y2 - координаты начала линии (x1, y1) и координаты конца
линии (x2, y2). Могут быть произвольными числами. В объектах класса Line
должны создаваться соответствующие локальные атрибуты с именами x1, y1, x2, y2.

В классе Line определить магический метод __len__() так, чтобы функция:

bool(line)
возвращала False, если длина линии меньше 1.

P.S. На экран ничего выводить не нужно. Только объявить класс.
"""


class Line:
    """Класс, представляющий линию на плоскости.

    :ivar x1: Координата X начала линии
    :ivar y1: Координата Y начала линии
    :ivar x2: Координата X конца линии
    :ivar y2: Координата Y конца линии
    """

    def __init__(self, x1: int | float, y1: int | float,
                 x2: int | float, y2: int | float) -> None:
        self.x1 = x1
        self.y1 = y1
        self.x2 = x2
        self.y2 = y2

    def __len__(self) -> int:
        """Вычисляет "длину" линии для функции bool().

        Возвращает 0, если длина линии меньше 1, иначе 1.
        Это позволяет функции bool(line) возвращать False для коротких линий.

        :return: 0 если длина < 1, иначе 1
        """
        return 0 if ((self.x2 - self.x1)**2 + (self.y2 - self.y1)**2
                     ) ** 0.5 < 1 else 1

