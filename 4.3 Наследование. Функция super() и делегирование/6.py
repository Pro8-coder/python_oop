"""
Объявите класс SoftList, который наследуется от стандартного класса list.
В классе SoftList следует объявить необходимые магические методы так, чтобы
при обращении к несуществующему элементу (по индексу) возвращалось значение
False (а не исключение Out of Range). Например:

sl = SoftList("python")
sl[0] # 'p'
sl[-1] # 'n'
sl[6] # False
sl[-7] # False
P.S. В программе нужно объявить только класс. На экран выводить ничего
не нужно.
"""
from typing import Any


class SoftList(list):
    """
    Класс SoftList, наследующий от стандартного list.

    Переопределяет поведение при обращении по индексу: вместо исключения
    IndexError возвращает False при попытке доступа к несуществующему элементу.
    """

    def __getitem__(self, item: int) -> Any | bool:
        """
        Получение элемента списка по индексу.

        :param item: Индекс элемента
        :return: Элемент списка или False, если индекс вне границ
        """
        try:
            return super().__getitem__(item)
        except IndexError:
            return False

